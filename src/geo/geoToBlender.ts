import { geoDistance, geoInterpolate } from "d3-geo";
import { readFile, writeFile } from "node:fs/promises";
import geoTo3D from "./geoTo3D.ts";
import {
  type GeoJsonObject,
  getLines,
  type Position,
  projectFlatCoordinate,
  roundValue,
} from "./helpers/blenderProjection.ts";

/** A fitted flat projection that converts longitude/latitude coordinates to x/y coordinates. */
export type GeoToBlenderFlatProjection = (
  coordinates: [number, number],
) => [number, number] | null;

/** A projection function or `"orthographic"` for 3D globe borders. */
export type GeoToBlenderProjection =
  | "orthographic"
  | GeoToBlenderFlatProjection;

/** Options for converting GeoJSON borders into a Blender OBJ file. */
export type GeoToBlenderOptions = {
  /**
   * For `"orthographic"`, the radius of the sphere in Blender units.
   * @default 10
   */
  radius?: number;
  /**
   * The number of decimal places to round OBJ coordinates to.
   */
  decimals?: number;
  /**
   * The maximum angular distance, in degrees, between generated vertices for `"orthographic"`.
   * By default, segments are not densified.
   */
  maxSegmentLength?: number;
};

function vertexLine(
  x: number,
  y: number,
  z: number,
  decimals?: number,
): string {
  return `v ${roundValue(x, decimals)} ${roundValue(y, decimals)} ${
    roundValue(z, decimals)
  }`;
}

function toObj(lines: [number, number, number][][], decimals?: number): string {
  const output = ["# Generated by @nshiab/journalism-geo", "o borders"];
  let vertexIndex = 1;

  for (const line of lines) {
    if (line.length < 2) {
      continue;
    }

    const indexes = [];

    for (const [x, y, z] of line) {
      output.push(vertexLine(x, y, z, decimals));
      indexes.push(vertexIndex);
      vertexIndex += 1;
    }

    output.push(`l ${indexes.join(" ")}`);
  }

  return `${output.join("\n")}\n`;
}

function densifyLine(line: Position[], maxSegmentLength: number): Position[] {
  if (line.length < 2) {
    return line;
  }

  const densified = [line[0]];

  for (let index = 1; index < line.length; index += 1) {
    const start = line[index - 1];
    const end = line[index];
    const distance = geoDistance(start, end) * (180 / Math.PI);
    const segments = Math.max(1, Math.ceil(distance / maxSegmentLength));
    const interpolate = geoInterpolate(start, end);

    for (let segment = 1; segment <= segments; segment += 1) {
      densified.push(interpolate(segment / segments) as Position);
    }
  }

  return densified;
}

/**
 * Converts GeoJSON borders into an OBJ file that can be imported in Blender.
 *
 * Polygon rings, MultiPolygon rings, LineStrings, and MultiLineStrings are exported as OBJ line geometry.
 * Polygon and MultiPolygon rings are always closed into loops (a closing coordinate is appended when the source data omits it), while LineStrings and MultiLineStrings are left exactly as provided.
 * Flat projection functions are placed on Blender's X/Y plane with Z up and north toward positive Y.
 * The `"orthographic"` projection exports borders in 3D on a sphere using `geoTo3D`.
 * Set `maxSegmentLength` to add extra vertices along long orthographic segments.
 *
 * When fitting a flat projection with `fitExtent`/`fitSize`, run the GeoJSON through {@link rewind} first. Many data sources wind their polygons the wrong way for `d3-geo`, which makes the fit collapse to a near-zero scale and produces an empty-looking OBJ.
 *
 * @param geojsonPath - The path to the GeoJSON file to convert.
 * @param projection - A flat projection function or `"orthographic"` for 3D globe borders.
 * @param outputPath - The path where the OBJ file will be written.
 * @param options - Optional settings for orthographic radius, coordinate rounding, and orthographic segment densification.
 * @returns A Promise that resolves to the output path.
 *
 * @example
 * ```ts
 * import { geoToBlender, rewind } from "@nshiab/journalism-geo";
 * import { geoConicConformal } from "d3-geo";
 * import { readFile } from "node:fs/promises";
 *
 * const geojsonPath = "./data/canada.geojson";
 * const geojson = JSON.parse(await readFile(geojsonPath, "utf8"));
 * // Rewind so fitExtent measures the real extent instead of collapsing.
 * const domain = rewind(geojson);
 * const projection = geoConicConformal()
 *   .rotate([100, -60])
 *   .fitExtent([[-5, -5], [5, 5]], domain);
 * await geoToBlender(geojsonPath, projection, "./output/canada.obj", {
 *   decimals: 3,
 * });
 * ```
 * @category Geo
 */
export default async function geoToBlender(
  geojsonPath: string,
  projection:
    | "orthographic"
    | ((coordinates: [number, number]) => [number, number] | null),
  outputPath: string,
  options: {
    /**
     * For `"orthographic"`, the radius of the sphere in Blender units.
     * @default 10
     */
    radius?: number;
    /**
     * The number of decimal places to round OBJ coordinates to.
     */
    decimals?: number;
    /**
     * The maximum angular distance, in degrees, between generated vertices for `"orthographic"`.
     * By default, segments are not densified.
     */
    maxSegmentLength?: number;
  } = {},
): Promise<string> {
  const radius = options.radius ?? 10;
  const geojson = JSON.parse(
    await readFile(geojsonPath, "utf8"),
  ) as GeoJsonObject;
  const lines = getLines(geojson);

  let objLines: [number, number, number][][];

  if (projection === "orthographic") {
    objLines = lines.map((line) =>
      (typeof options.maxSegmentLength === "number"
        ? densifyLine(line, options.maxSegmentLength)
        : line).map(([lon, lat]) =>
          geoTo3D(lon, lat, radius, {
            decimals: options.decimals,
            toArray: true,
          })
        )
    );
  } else {
    objLines = lines.map((line) =>
      line.flatMap(([lon, lat]) => {
        const projected = projectFlatCoordinate(
          projection,
          lon,
          lat,
          options.decimals,
        );

        if (projected === null) {
          return [];
        }

        return [projected];
      })
    );
  }

  await writeFile(outputPath, toObj(objLines, options.decimals));

  return outputPath;
}
